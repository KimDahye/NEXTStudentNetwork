package com.example.nextpiki;

public class Content {
	public String backImage;
	public String stuImage;
	
	public String stuName;
	public String stuMajor;
	public String stuVision;
	
	public Content(String backImage, String stuImage, String stuName,
			String stuMajor, String stuVision) {
		super();
		this.backImage = backImage;
		this.stuImage = stuImage;
		this.stuName = stuName;
		this.stuMajor = stuMajor;
		this.stuVision = stuVision;
	}
	
	
}
